<template>
  <div>
    <el-button type="info" @click="logout">Logout</el-button>
  </div>
</template>

<script>
export default {
  methods: {
    logout() {
      window.sessionStorage.clear()
      this.$message.success('退出成功')
      this.$router.push('/login')
    }
  }
}
</script>

